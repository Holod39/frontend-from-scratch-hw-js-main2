function capitalizeWords(inputString) {
  // –†–∞–∑–±–∏–≤–∞–µ–º —Å—Ç—Ä–æ–∫—É –Ω–∞ —Å–ª–æ–≤–∞
  const words = inputString.split(' ');
  // –°–æ–∑–¥–∞–µ–º –ø—É—Å—Ç–æ–π –º–∞—Å—Å–∏–≤ –¥–ª—è —Ö—Ä–∞–Ω–µ–Ω–∏—è –∑–∞–≥–ª–∞–≤–Ω—ã—Ö —Å–ª–æ–≤
  const capitalizedWords = [];

  // –ü—Ä–æ—Ö–æ–¥–∏–º –ø–æ –∫–∞–∂–¥–æ–º—É —Å–ª–æ–≤—É –≤ –º–∞—Å—Å–∏–≤–µ
  for (let i = 0; i < words.length; i++) {
      // –î–µ–ª–∞–µ–º –ø–µ—Ä–≤–æ–µ —Å–ª–æ–≤–æ –∑–∞–≥–ª–∞–≤–Ω—ã–º –∏ –¥–æ–±–∞–≤–ª—è–µ–º –≤ –º–∞—Å—Å–∏–≤
      const capitalizedWord = words[i].charAt(0).toUpperCase() + words[i].slice(1).toLowerCase();
      capitalizedWords.push(capitalizedWord);
  }

  // –û–±—ä–µ–¥–∏–Ω—è–µ–º —Å–ª–æ–≤–∞ –æ–±—Ä–∞—Ç–Ω–æ –≤ —Å—Ç—Ä–æ–∫—É
  return capitalizedWords.join(' ');
}